<div class="question">
Какая минимальная/максимальная сумма ставки?
</div>
<div class="answer">
Ограничений нет, вы можете поставить хоть 1 копейку, хоть 1000 рублей. Не стоит забывать, что чем больше сумма ставки, тем больше вы выиграете. Так, например, вы можете сделать крупную ставку и забрать себе почти всю сумму от проигравшей рыбы.
</div>
