Есть ли хитрости в игре?
</div>
<div class="answer">
Если вы выдите, что ваша рыба проигрывает, вы можете на последних минутах сделать решающую ставку и сделать вашу рыбу победителем.<br />
Если вы видите, что одна из рыб набрала хорошую сумму, вы можете на последних минутах поставить на другую рыбу большую ставку и практически забрать себе всю сумму проигравшей рыбы.
