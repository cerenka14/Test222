<div class="faq">
<div class="question">
Как определяется размер выигрыша? zhf.su
</div>
<div class="answer">
Всё очень просто, по истечении времени рассчитывается процент вашего вклада в победившую рыбу, например, ваш вклад составляет 75% от суммы всех вкладов. 
Соответственно, вы получите обратно свою ставку + 75% от суммы проигравшей рыбы. Поэтому чем больше ставка, тем больше процент вашего финансового участия, тем больше ваш выигрыш.
</div>
</div>
<div style="clear:both"></div>

<div class="faq">
<div class="question">
Как происходит ввод/вывод средств?
</div>
<div class="answer">
Ввод и выплаты осуществляются в автоматиеском режиме через платёжную систему Payeer. В проекте не нужно регистрироваться. В момент розыгрыша если рыба, на которую вы сделали ставку выиграла, 
ваш выигрыш будет отправлен обратно на кошелёк, с которого была сделана ставка.
</div>
</div>
<div style="clear:both"></div>

<div class="faq">
<div class="question">
Как работвет рефсистема?
</div>
<div class="answer">
Вы получаете 5% от выигрыша ваших друзей, которые перешли по вашей реферальной ссылке. Система сама запоминает с какого кошелька и по какой рефссылке была сделана 
ставка и создаёт реферальное соотношение. То есть, ваш друг перешёл по рефссылке, сделал ставку и выиграл, вы сразу получите свой процент на ваш кошелёк. 
Даже если он сделает победную ставку со своего кошелька, но на другом браузере или устройстве, вы всё равно получите свой процент.
</div>
</div>
<div style="clear:both"></div>

